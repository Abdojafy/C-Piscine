#include<unistd.h>
int	main(int ac, char**av)
{
	char	c;
	int		i;
	int		j;

	i = 0;
	j = 1;
	while (j <= ac - 1)
	{
		i = 0;
		while (av[j][i])
		{
			c = av[j][i];
			write(1, &c, 1);
			i++;
		}
		write(1, "\n", 1);
		j++;
	}
}
