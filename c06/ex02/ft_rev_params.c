#include<unistd.h>
int	main(int ac, char**av)
{
	char	c;
	int		i;
	int		j;

	i = 0;
	j = ac;
	while (j >= 2)
	{
		i = 0;
		while (av[j - 1][i])
		{
			c = av[j - 1][i];
			write(1, &c, 1);
			i++;
		}
		write(1, "\n", 1);
		j--;
	}
}
